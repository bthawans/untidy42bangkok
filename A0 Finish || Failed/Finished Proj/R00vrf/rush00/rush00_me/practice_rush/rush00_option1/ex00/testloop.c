#include <unistd.h>
#include <stdio.h>

int main()
{
    int a;
    int b;
    int c;

    a = 5;
    b = 0;
    while (b <= a)
    {
        c = 0;
        while (c <= b)
        {
            write(1, "*",1);
            c++;
        }
        write(1, "\n",1);
        b++;
    }
}

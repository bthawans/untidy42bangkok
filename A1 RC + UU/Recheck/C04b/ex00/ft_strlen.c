#include <stdio.h>
#include <string.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int main()
{
	char a[] = "bamboo";
	int b = ft_strlen(a);
	printf("%d\n", b);
	return (0);
}

#include <stdio.h>
#include <string.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] - s2[i]);
		}
		i++; 
	}
	return (0);
}

int main()
{
	int	num;
	num = ft_strncmp("ABCDEFG", "ABCDOGE", 4);
	printf("%d\n", num);
	return(0);
}


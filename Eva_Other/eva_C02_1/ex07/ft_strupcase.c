char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 97 && str[i] <= 122)
			str[i] = str[i] - 32;
		i ++;
	}
	return (str);
}

#include <stdio.h>

int	main(void)
{
	char text[] = "hEllo wOrld";
	printf("%s", ft_strupcase(text));
}